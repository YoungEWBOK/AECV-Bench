def split_prompt_strategy_suffix(raw_model_name: str):
    """Split a prompt-strategy suffix from a raw model result name."""
    for suffix in sorted(PROMPT_STRATEGY_NAMES, key=len, reverse=True):
        token = f"_{suffix}"
        if raw_model_name.endswith(token):
            return raw_model_name[:-len(token)], suffix
    return raw_model_name, None


def official_model_display_name(raw_model_name: str) -> str:
    """Return a display name that preserves prompt-strategy variants."""
    base_name, strategy = split_prompt_strategy_suffix(raw_model_name)
    model_name = OFFICIAL_MODEL_NAMES.get(base_name, base_name.replace('_', ' ').title())
    if strategy:
        return f"{model_name} ({PROMPT_STRATEGY_NAMES[strategy]})"
    return model_name


def raw_model_base_name(raw_model_name: str) -> str:
    """Return raw model name without a prompt-strategy suffix."""
    return split_prompt_strategy_suffix(raw_model_name.lower())[0]

